package application;

public class StringHeightAndWeight {
	String heightValue;
	String weightValue;
	public StringHeightAndWeight(String Height,String Weight) {
		this.heightValue = Height;
		this.weightValue = Weight;
	}
	public String getHeight() {
		return this.heightValue;
	}
	
	public String getWeight() {
		return this.weightValue;
	}
}
